    private void jBLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLoginActionPerformed
        try {
            // TODO add your handling code here:
            String usuario = jTUsuario.getText().substring(0, 3).toUpperCase();
            switch (usuario) {
                case "ADM":
                    Admin ventanaAdmin = new Admin(this, true);
                    ventanaAdmin.setLocationRelativeTo(null);
                    ventanaAdmin.setVisible(true);
                    this.listaEstudiante = ventanaAdmin.getListaEstudianteCreada();
                    this.listaProfesores = ventanaAdmin.getListaProfesorCreada();
                    this.listaCursos = ventanaAdmin.getListaCursoCreado();
                    break;
                case "EST":
                    EstudianteInterfaz ventanaEstudiante = new EstudianteInterfaz(this, listaEstudiante, listaProfesores, listaCursos, true);
                    ventanaEstudiante.setLocationRelativeTo(null);
                    ventanaEstudiante.setVisible(true);
                    break;
                case "PRF":
                    ProfesorInterfaz ventanaProfesor = new ProfesorInterfaz(this, true);
                    ventanaProfesor.setLocationRelativeTo(null);
                    ventanaProfesor.setVisible(true);
                    break;
                default:
                    throw new AssertionError();
            }
        } catch (Exception ex) {
            Logger.getLogger(LoginPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jBLoginActionPerformed

